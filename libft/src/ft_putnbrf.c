#include "libft.h"

void	ft_putnbrf(int n, int fd)
{
	if (n < 0)
		n = -n;
	if (n == -2147483648)
	{
		ft_putchar_fd('2', fd);
		n = n % 1000000000;
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbrf(n / 10, fd);
		ft_putnbrf(n % 10, fd);
	}
	else
		ft_putchar_fd(n + '0', fd);
}
